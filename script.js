function setLanguage(lang) {
    const elements = document.querySelectorAll('[data-key]');
    elements.forEach(element => {
        const key = element.getAttribute('data-key');
        if (translations[lang][key]) {
            element.innerHTML = translations[lang][key];
        }
    });

    document.documentElement.lang = lang;

    // Atualiza o botão ativo
    const buttons = document.querySelectorAll('.language-switcher button');
    buttons.forEach(button => {
        button.classList.remove('active');
    });
    document.querySelector(`.language-switcher button[onclick="setLanguage('${lang}')"]`).classList.add('active');
}

// Sistema de partículas flutuantes com nova paleta Cyberpunk Elegante
function createParticles() {
    const particlesContainer = document.getElementById('particles');
    const particleCount = 40; // Reduzido para melhor performance
    
    for (let i = 0; i < particleCount; i++) {
        const particle = document.createElement('div');
        particle.className = 'particle';
        
        // Posição aleatória
        particle.style.left = Math.random() * 100 + '%';
        particle.style.animationDelay = Math.random() * 6 + 's';
        particle.style.animationDuration = (Math.random() * 3 + 4) + 's';
        
        // Tamanho aleatório
        const size = Math.random() * 2 + 1;
        particle.style.width = size + 'px';
        particle.style.height = size + 'px';
        
        // Cores da nova paleta Cyberpunk Elegante
        const colors = ['#00BCD4', '#64FFDA', '#2E3138', '#1A1A1D'];
        particle.style.background = colors[Math.floor(Math.random() * colors.length)];
        
        particlesContainer.appendChild(particle);
    }
}

// Efeito de digitação para o título principal
function typeWriterEffect(element, text, speed = 100) {
    let i = 0;
    element.innerHTML = '';
    
    function type() {
        if (i < text.length) {
            element.innerHTML += text.charAt(i);
            i++;
            setTimeout(type, speed);
        }
    }
    
    type();
}

// Efeito de entrada para elementos
function animateOnScroll() {
    const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.style.opacity = '1';
                entry.target.style.transform = 'translateY(0)';
            }
        });
    });

    const elements = document.querySelectorAll('.job, .skill-card, .education-item');
    elements.forEach(el => {
        el.style.opacity = '0';
        el.style.transform = 'translateY(30px)';
        el.style.transition = 'opacity 0.6s ease, transform 0.6s ease';
        observer.observe(el);
    });
}

// Efeito de cursor piscante para o título
function addCursorEffect() {
    const title = document.querySelector('#hero h1');
    if (title) {
        title.innerHTML += '<span class="cursor">|</span>';
        
        setInterval(() => {
            const cursor = title.querySelector('.cursor');
            if (cursor) {
                cursor.style.opacity = cursor.style.opacity === '0' ? '1' : '0';
            }
        }, 500);
    }
}

// Efeito de glitch para o logo com nova paleta
function addGlitchEffect() {
    const logo = document.querySelector('.logo');
    if (logo) {
        setInterval(() => {
            if (Math.random() < 0.08) { // 8% de chance de ativar o glitch
                logo.style.textShadow = `
                    2px 0 #64FFDA,
                    -2px 0 #00BCD4,
                    0 2px #2E3138,
                    0 -2px #1A1A1D
                `;
                logo.style.transform = 'skew(1.5deg)';
                
                setTimeout(() => {
                    logo.style.textShadow = '';
                    logo.style.transform = 'skew(0deg)';
                }, 150);
            }
        }, 4000);
    }
}

// Efeito de hover 3D para os cards
function add3DHoverEffect() {
    const cards = document.querySelectorAll('.skill-card, .job, .education-item');
    
    cards.forEach(card => {
        card.addEventListener('mousemove', (e) => {
            const rect = card.getBoundingClientRect();
            const x = e.clientX - rect.left;
            const y = e.clientY - rect.top;
            
            const centerX = rect.width / 2;
            const centerY = rect.height / 2;
            
            const rotateX = (y - centerY) / 15; // Reduzido para movimento mais sutil
            const rotateY = (centerX - x) / 15;
            
            card.style.transform = `perspective(1000px) rotateX(${rotateX}deg) rotateY(${rotateY}deg) translateZ(15px)`;
        });
        
        card.addEventListener('mouseleave', () => {
            card.style.transform = 'perspective(1000px) rotateX(0deg) rotateY(0deg) translateZ(0px)';
        });
    });
}

// Efeito de brilho sutil nos elementos
function addSubtleGlow() {
    const glowElements = document.querySelectorAll('.skill-card, .job, .education-item');
    
    glowElements.forEach(element => {
        element.addEventListener('mouseenter', () => {
            element.style.boxShadow = `0 0 25px rgba(0, 188, 212, 0.15)`;
        });
        
        element.addEventListener('mouseleave', () => {
            element.style.boxShadow = '';
        });
    });
}

// Inicialização quando o DOM estiver carregado
document.addEventListener('DOMContentLoaded', () => {
    // Define o idioma inicial
    const userLang = navigator.language.split('-')[0] || 'pt';
    const langToSet = translations[userLang] ? userLang : 'pt';
    setLanguage(langToSet);
    
    // Cria as partículas
    createParticles();
    
    // Adiciona efeitos visuais
    animateOnScroll();
    addCursorEffect();
    addGlitchEffect();
    add3DHoverEffect();
    addSubtleGlow();
    
    // Efeito de entrada para o título principal
    const heroTitle = document.querySelector('#hero h1');
    if (heroTitle) {
        const originalText = heroTitle.textContent;
        setTimeout(() => {
            typeWriterEffect(heroTitle, originalText, 80);
        }, 1000);
    }
});

// Efeito de scroll suave
document.querySelectorAll('a[href^="#"]').forEach(anchor => {
    anchor.addEventListener('click', function (e) {
        e.preventDefault();
        const target = document.querySelector(this.getAttribute('href'));
        if (target) {
            target.scrollIntoView({
                behavior: 'smooth',
                block: 'start'
            });
        }
    });
});
